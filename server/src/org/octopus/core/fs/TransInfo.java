package org.octopus.core.fs;

public class TransInfo {

    private int orgWidth;

    private int orgHeight;

    private int cutX;

    private int cutY;

    private int cutWidth;

    private int cutHeight;

    public int getOrgWidth() {
        return orgWidth;
    }

    public void setOrgWidth(int orgWidth) {
        this.orgWidth = orgWidth;
    }

    public int getOrgHeight() {
        return orgHeight;
    }

    public void setOrgHeight(int orgHeight) {
        this.orgHeight = orgHeight;
    }

    public int getCutX() {
        return cutX;
    }

    public void setCutX(int cutX) {
        this.cutX = cutX;
    }

    public int getCutY() {
        return cutY;
    }

    public void setCutY(int cutY) {
        this.cutY = cutY;
    }

    public int getCutWidth() {
        return cutWidth;
    }

    public void setCutWidth(int cutWidth) {
        this.cutWidth = cutWidth;
    }

    public int getCutHeight() {
        return cutHeight;
    }

    public void setCutHeight(int cutHeight) {
        this.cutHeight = cutHeight;
    }

}
